package DemoLinkList;

public class Node {
 int data;
 Node next;
public Node() {
	this.data=0;
	this.next=null;
}
public int getData() {
	return data;
}
public void setData(int data) {
	this.data = data;
}
public Node getNode() {
	return next;
}
public void setNode(int data) {
	this.data = data;
}

}

